const fs = require("fs");
const path = require("path");

const DEFAULT_BATCH_SIZE = 200;
const DEFAULT_ADMIN_SAFE = "0x158B985CC667b4E022AD05B99E89007790da66E2";

function getDefaultDeploymentPath(networkName) {
  return path.resolve(__dirname, "..", "..", "deployments", `${networkName}-latest.json`);
}

function parseArgs(argv) {
  const options = {
    address: null,
    batchFile: null,
    outFile: null,
    safeTx: false,
    useDeploySigner: false,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === "--batch") {
      options.batchFile = argv[++i];
    } else if (arg === "--out") {
      options.outFile = argv[++i];
    } else if (arg === "--safe-tx") {
      options.safeTx = true;
    } else if (arg === "--use-deploy-signer") {
      options.useDeploySigner = true;
    } else if (!options.address) {
      options.address = arg;
    } else {
      throw new Error(`Unexpected argument: ${arg}`);
    }
  }

  return options;
}

function ensureExpectedNetwork(runtime, deployment) {
  const runtimeName = runtime.network.name;
  if (deployment.network && deployment.network !== runtimeName) {
    throw new Error(
      `Deployment artifact network mismatch: expected ${runtimeName}, found ${deployment.network}`
    );
  }

  const runtimeChainId = String(runtime.network.config.chainId ?? "");
  if (deployment.chainId && runtimeChainId && String(deployment.chainId) !== runtimeChainId) {
    throw new Error(
      `Deployment artifact chainId mismatch: expected ${runtimeChainId}, found ${deployment.chainId}`
    );
  }
}

function loadDeployment(runtime, overridePath) {
  const deploymentPath = path.resolve(
    overridePath || getDefaultDeploymentPath(runtime.network.name)
  );
  if (!fs.existsSync(deploymentPath)) {
    throw new Error(`Deployment artifact not found: ${deploymentPath}`);
  }

  const deployment = JSON.parse(fs.readFileSync(deploymentPath, "utf8"));
  ensureExpectedNetwork(runtime, deployment);
  return { deploymentPath, deployment };
}

function resolveWhitelistAddress(runtime, deployment) {
  const overrideAddress = process.env.WHITELIST_ADDRESS;
  if (overrideAddress) {
    const address = runtime.ethers.getAddress(overrideAddress);
    if (address === runtime.ethers.ZeroAddress) {
      throw new Error("WHITELIST_ADDRESS cannot be zero address");
    }
    return { address, source: "WHITELIST_ADDRESS" };
  }

  const artifactAddress = deployment.contracts?.PurchaserWhitelist;
  if (!artifactAddress) {
    throw new Error("Deployment artifact is missing contracts.PurchaserWhitelist");
  }

  const address = runtime.ethers.getAddress(artifactAddress);
  if (address === runtime.ethers.ZeroAddress) {
    throw new Error("Deployment artifact purchaser whitelist cannot be zero address");
  }

  return { address, source: "deployment artifact" };
}

function normalizeAddress(runtime, address) {
  const normalized = runtime.ethers.getAddress(address);
  if (normalized === runtime.ethers.ZeroAddress) {
    throw new Error("Address cannot be zero address");
  }
  return normalized;
}

function loadBatchAddresses(runtime, batchFile) {
  const resolvedPath = path.resolve(batchFile);
  const parsed = JSON.parse(fs.readFileSync(resolvedPath, "utf8"));
  if (!parsed || !Array.isArray(parsed.addresses)) {
    throw new Error("Batch file must be JSON with an addresses array");
  }

  return parsed.addresses.map((address) => normalizeAddress(runtime, address));
}

function chunkAddresses(addresses, batchSize = DEFAULT_BATCH_SIZE) {
  const chunks = [];
  for (let i = 0; i < addresses.length; i += batchSize) {
    chunks.push(addresses.slice(i, i + batchSize));
  }
  return chunks;
}

function resolveAdminSafe(deployment) {
  return process.env.ADMIN_SAFE_ADDRESS || deployment.governance?.adminSafe || DEFAULT_ADMIN_SAFE;
}

function buildSafeTransaction({ runtime, deployment, whitelistAddress, method, addresses }) {
  if (!["addBatch", "removeBatch"].includes(method)) {
    throw new Error(`Unsupported Safe whitelist method: ${method}`);
  }

  const normalized = addresses.map((address) => normalizeAddress(runtime, address));
  if (normalized.length === 0) {
    throw new Error("At least one address is required");
  }

  const iface = new runtime.ethers.Interface([
    "function addBatch(address[] accounts)",
    "function removeBatch(address[] accounts)",
  ]);

  const transactions = chunkAddresses(normalized).map((batch) => ({
    to: whitelistAddress,
    value: "0",
    data: iface.encodeFunctionData(method, [batch]),
    contractMethod: null,
    contractInputsValues: null,
  }));

  return {
    version: "1.0",
    chainId: String(deployment.chainId || runtime.network.config.chainId),
    createdAt: Date.now(),
    meta: {
      name: `Purchaser whitelist ${method === "addBatch" ? "add" : "remove"}`,
      description: `Generated by scripts/whitelist-mainnet.js for ${normalized.length} wallet(s)`,
    },
    transactions,
    safe: resolveAdminSafe(deployment),
  };
}

async function getRoleStatus(runtime, options = {}) {
  const { whitelist, address, deployment } = await getWhitelistContract(runtime, options);
  const defaultAdminRole = await whitelist.DEFAULT_ADMIN_ROLE();
  const whitelistAdminRole = await whitelist.WHITELIST_ADMIN_ROLE();
  const signers = options.signer ? [options.signer] : await runtime.ethers.getSigners();
  const signerAddress = signers[0] ? await signers[0].getAddress() : null;
  const adminSafe = runtime.ethers.getAddress(resolveAdminSafe(deployment));
  const deploymentDeployer = deployment.deployer
    ? runtime.ethers.getAddress(deployment.deployer)
    : null;

  return {
    whitelist: address,
    adminSafe,
    deploymentDeployer,
    signer: signerAddress,
    defaultAdminRole,
    whitelistAdminRole,
    adminSafeHasDefaultAdminRole: await whitelist.hasRole(defaultAdminRole, adminSafe),
    adminSafeHasWhitelistAdminRole: await whitelist.hasRole(whitelistAdminRole, adminSafe),
    deploymentDeployerHasDefaultAdminRole: deploymentDeployer
      ? await whitelist.hasRole(defaultAdminRole, deploymentDeployer)
      : null,
    deploymentDeployerHasWhitelistAdminRole: deploymentDeployer
      ? await whitelist.hasRole(whitelistAdminRole, deploymentDeployer)
      : null,
    signerHasWhitelistAdminRole: signerAddress
      ? await whitelist.hasRole(whitelistAdminRole, signerAddress)
      : false,
  };
}

async function getWhitelistContract(runtime, options = {}) {
  const { deploymentPath, deployment } = loadDeployment(runtime, options.deploymentPath);
  const { address, source } = resolveWhitelistAddress(runtime, deployment);
  const signer = options.signer || (await runtime.ethers.getSigners())[0];
  const whitelist = signer
    ? new runtime.ethers.Contract(
        address,
        (await runtime.artifacts.readArtifact("PurchaserWhitelist")).abi,
        signer
      )
    : await runtime.ethers.getContractAt("PurchaserWhitelist", address);
  return { whitelist, signer, address, source, deployment, deploymentPath };
}

async function add(runtime, address, options = {}) {
  const { whitelist, signer } = await getWhitelistContract(runtime, options);
  const normalized = normalizeAddress(runtime, address);
  const tx = await sendContractTransaction(whitelist, signer, "addToWhitelist", [normalized]);
  const receipt = await tx.wait();
  return { address: normalized, txHash: receipt.hash, gasUsed: receipt.gasUsed.toString() };
}

async function remove(runtime, address, options = {}) {
  const { whitelist, signer } = await getWhitelistContract(runtime, options);
  const normalized = normalizeAddress(runtime, address);
  const tx = await sendContractTransaction(whitelist, signer, "removeFromWhitelist", [normalized]);
  const receipt = await tx.wait();
  return { address: normalized, txHash: receipt.hash, gasUsed: receipt.gasUsed.toString() };
}

async function check(runtime, address, options = {}) {
  const { whitelist } = await getWhitelistContract(runtime, options);
  const normalized = normalizeAddress(runtime, address);
  const isWhitelisted = await whitelist.isWhitelisted(normalized);
  return { address: normalized, isWhitelisted };
}

async function runBatch(runtime, method, addresses, options = {}) {
  const { whitelist, signer } = await getWhitelistContract(runtime, options);
  const normalized = addresses.map((address) => normalizeAddress(runtime, address));
  const chunks = chunkAddresses(normalized);
  let gasUsedTotal = 0n;
  const receipts = [];

  for (const batch of chunks) {
    const tx = await sendContractTransaction(whitelist, signer, method, [batch]);
    const receipt = await tx.wait();
    gasUsedTotal += receipt.gasUsed;
    receipts.push({
      size: batch.length,
      txHash: receipt.hash,
      gasUsed: receipt.gasUsed.toString(),
    });
  }

  return {
    count: normalized.length,
    chunks: receipts.length,
    gasUsedTotal: gasUsedTotal.toString(),
    receipts,
  };
}

async function sendContractTransaction(contract, signer, method, args) {
  if (!signer) {
    throw new Error("No signer available for whitelist transaction");
  }

  if (typeof signer.sendTransaction === "function" && signer.constructor.name !== "KmsSigner") {
    return contract[method](...args);
  }

  if (!signer.provider || typeof signer.signTransaction !== "function") {
    throw new Error("Signer cannot broadcast whitelist transaction");
  }

  const request = await contract[method].populateTransaction(...args);
  const populated = await signer.populateTransaction({
    ...request,
    from: await signer.getAddress(),
  });
  const signed = await signer.signTransaction(populated);
  return signer.provider.broadcastTransaction(signed);
}

async function addBatch(runtime, addresses, options = {}) {
  return runBatch(runtime, "addBatch", addresses, options);
}

async function removeBatch(runtime, addresses, options = {}) {
  return runBatch(runtime, "removeBatch", addresses, options);
}

